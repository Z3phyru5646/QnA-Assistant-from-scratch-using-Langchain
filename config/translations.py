    """Get translated string for the current UI language."""
    try:
        lang_code = LANGUAGES.get(
            st.session_state.get("ui_language", "English"), "en"
        )
        return TRANSLATIONS.get(key, {}).get(lang_code, TRANSLATIONS.get(key, {}).get("en", key))
    except Exception:
        return TRANSLATIONS.get(key, {}).get("en", key)
